function positionFromGuide(unit: GuideUnitDefinition, definition: GuidePositionDefinition): Position {
  const kpis = [...definition.kpis];
  const unitIndicators = unit.unitIndicators.map(unitIndicatorKpi);
  const allKpis = definition.name.toLocaleLowerCase("es-MX").includes("director")
    ? [...kpis, ...unitIndicators]
    : kpis;
  return {
    id: createPositionId(unit.id, definition.slug),
    businessUnitId: unit.id,
    area: definition.level,
    name: definition.name,
    rep: definition.rep,
    kpis: allKpis,
    unitIndicatorLinks: definition.unitIndicatorLinks,
    definitionSource: unit.sourceFile,
    physicalFormatIds: [`${unit.id}-evaluacion-${definition.slug}`],
    suggestedFrequency: "Diaria / Semanal",
    suggestedEvidence: "Focus diario, indicadores del puesto, metas vigentes y evidencia documental del REP.",
    suggestedEvaluator: "Responsable directo del organigrama",
    status: "active",
    isEvaluable: Boolean(definition.rep.trim() && hasOfficialKpis(allKpis)),
  };
}

